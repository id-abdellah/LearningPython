# python data types:

# int => Integers
print(type(100))
print(type(-200))

# float => Floating point number
print(type(100.200))
print(type(-1.9))

# str => Strings
print(type("Ahhhm"))

# list => List.
# خارجي module خاص arrays فاللغات اللخرين ولكن هنايا فالبايثون باش تعامل مع ال arrays بحال ال list ال
# numpy هو module هاد ال
print(type([1,2,3,4,5]))

# tuple => Tuple
print(type((1,2,3,4,5)))

# dict => Dictionary
# js فال object هنايا هو بحال ال dictionary ال
print(type({"one": 1, "two": 2, "three": 3}))

# bool => Boolean
print(type(True))
print(type(2 == 4))



